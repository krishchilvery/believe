from django.core.exceptions import ImproperlyConfigured

with open(os.environ.get('BELIEVE_CONFIG')) as f:
    configs = json.loads(f.read())

def get_env_var(setting, configs=configs):
    try:
        val = configs[setting]
        if val == 'True':
            val = True
        elif val == 'FALSE':
            val = False
        return val
    except KeyError:
        error_msg = "ImproperlyConfigured: Set {0} environment variable".format(error_msg)
        raise ImproperlyConfigured(error_msg)            
            
